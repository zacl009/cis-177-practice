// Log the numbers from 1 to 10.
// In parenthesis, specify 3 things - start - stop - increment
// 'for' limits the use of GLOBAL VARIABLE for i
for (let i = 1; i <= 10; i++) {
  console.log(i);
}

// Add numbers from 1 to 10 using 'for'
let total = 0;
for (let i = 1; i <= 10; i++) {
  console.log(total);
  total += i;
}

